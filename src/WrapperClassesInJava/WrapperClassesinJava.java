package WrapperClassesInJava;

public class WrapperClassesinJava {
    public static void main(String[] args) {
           int b = 137;
//           Autoboxing primitive int => Integers objects
        Integer a = b;
        System.out.println("The Primitive int b is : "+b);
        System.out.println("The Integers Objects a is : "+a);
    }
}
