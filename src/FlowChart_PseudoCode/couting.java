package FlowChart_PseudoCode;

import java.util.Scanner;

public class couting {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the value of n is : ");
        int n = sc.nextInt();
        for(int i=0;i<n;i++){
            System.out.print(i+" ");
        }
    }
}
