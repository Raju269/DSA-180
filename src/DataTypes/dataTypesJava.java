package DataTypes;

public class dataTypesJava {
    public static void main(String[] args) {
//        Primitive Data Types in Java
        boolean isLoggined = true;
        System.out.println(isLoggined);
        byte moduleLevel = 34;
        System.out.println(moduleLevel);
        short contestScore = 1940;
        System.out.println(contestScore);
        int marks =23;
        System.out.println(marks);
        long distanceOfSun = 343325454343l;
        System.out.println(distanceOfSun);
        double meansValue = 234.434d;
        System.out.println(meansValue);
        char isSymbols = 'A';
        System.out.println(isSymbols);

//        Non Primitive Data Types in Java
//        Primitive vs Reference Types
//        String
//        Arrays
//        Classes
//        Objects
        String isMyName = "Raju kumar ";
        System.out.println(isMyName);
        String trackName = "DSA Mastery Track";
        System.out.println(trackName);

    }
}
