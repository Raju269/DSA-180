package Operators;

public class Assignment_Operator {
    public static void main(String[] args) {

        int ratingPoints = 100;
        ratingPoints +=20;
        System.out.println(ratingPoints);
        ratingPoints -=20;
        System.out.println(ratingPoints);
        ratingPoints *=20;
        System.out.println(ratingPoints);
        ratingPoints /=20;
        System.out.println(ratingPoints);
        ratingPoints %=20;
        System.out.println(ratingPoints);
    }
}
